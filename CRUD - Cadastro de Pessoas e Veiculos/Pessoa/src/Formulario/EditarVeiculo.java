    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        
        int idVeiculo = Integer.parseInt(txtIdVeiculo.getText()); // ID do veículo
        
        VeiculoDAO vDAO = new VeiculoDAO();
        
        Pessoa p = new Pessoa();
        
        p.setId(Integer.parseInt(txtIdDono.getText()));
        
        Veiculo v1 = new Veiculo();
        
        v1.setId(Integer.parseInt(txtIdVeiculo.getText()));
        v1.setModelo(txtModelo.getText());
        v1.setPlaca(txtPlaca.getText());
        v1.setPessoaid(p);

        vDAO.editar(v1);
        limparFormulario();
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?", "Exclusão",JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION){
            VeiculoDAO vDAO = new VeiculoDAO();
            vDAO.excluir(Integer.parseInt(txtIdVeiculo.getText())); // Exclui o veículo com o ID informado
            limparFormulario();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

