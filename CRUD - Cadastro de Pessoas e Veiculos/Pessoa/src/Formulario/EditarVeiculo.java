        private void limparFormulario(){
        txtIdVeiculo.setText("");
        txtModelo.setText("");
        txtPlaca.setText("");
        txtIdDono.setText("");
        txtId2.setText("");
        cmbDono.setSelectedIndex(-1);
    }
    
    private void ConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultarActionPerformed

        
        int idVeiculo = Integer.parseInt(txtIdVeiculo.getText());
        
        VeiculoDAO vDAO = new VeiculoDAO();
        Veiculo v = vDAO.getVeiculo(idVeiculo);
        if (v == null){
            limparFormulario();
            JOptionPane.showMessageDialog(this, "veiculo nao encontrada");
        }
        else{
            txtId2.setText(txtIdVeiculo.getText());
            txtModelo.setText(v.getModelo());
            txtPlaca.setText(v.getPlaca());
             
            pessoaRecuperada = v.getPessoaid();
            txtIdDono.setText(String.valueOf(pessoaRecuperada.getId()));
            
            Pessoa pessoaSelecionada = (Pessoa) v.getPessoaid();
            cmbDono.setSelectedItem(pessoaSelecionada);

        }
    }//GEN-LAST:event_ConsultarActionPerformed

