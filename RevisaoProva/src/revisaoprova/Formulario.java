    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        DefaultTableModel tabelaProfessor = (DefaultTableModel) tblProfessor.getModel();
        DefaultTableModel tabelaAluno = (DefaultTableModel) tblAluno.getModel();
        ArrayList<Professor> lista_professor = new ArrayList<Professor>();
        ArrayList<Aluno> lista_alunos = new ArrayList<Aluno>();
        if (rdbProfessor.isSelected()){
          Professor p = new Professor(txtMatriculaProfessor.getText(), txtNome.getText(), txtData.getText(), txtSexo.getText(), txtRg.getText(), txtEndereco.getText());
          lista_professor.add(p);
          
          for (Professor prof : lista_professor) {
          tabelaProfessor.addRow(prof.obterDados());
          }
          
          JOptionPane.showMessageDialog(null,p.executaAcao());
        }
        if (rdbAluno.isSelected()){
          Aluno a = new Aluno(txtMatriculaAluno.getText(), txtNome.getText(), txtData.getText(), txtSexo.getText(), txtRg.getText(), txtEndereco.getText());
          lista_alunos.add(a);
          
          for (Aluno al : lista_alunos) {
          tabelaAluno.addRow(al.obterDados());
          }
          
          JOptionPane.showMessageDialog(null,a.executaAcao());
        }
       
        
        
    }//GEN-LAST:event_btnSalvarActionPerformed

